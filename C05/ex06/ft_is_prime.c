#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	c;
	int	d;

	c = 1;
	d = 0;
	if (nb <= 1)
		return (0);
	while (c < nb)
	{
		if (nb % c == 0)
			d++;
		c++;
	}
	if (d == 1)
		return (1);
	return (0);
}
