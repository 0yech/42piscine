int	ft_str_is_numeric(char *str)
{
	int	a;

	a = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[a])
	{
		if (str[a] <= '9' && str[a] >= '0')
		{
			a++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
