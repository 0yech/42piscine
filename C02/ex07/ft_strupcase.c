#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (str[a] <= 'z' && str[a] >= 'a')
		{
			str[a] = str[a] - 32;
		}
	a++;
	}
	str[a] = 0;
	return (str);
}
