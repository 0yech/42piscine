int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[a])
	{
		if (
			(str[a] <= 'z' && str[a] >= 'a')
			|| (str[a] <= 'Z' && str[a] >= 'A')
		)
		{
			a++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
