#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (str[a] <= 'Z' && str[a] >= 'A')
		{
			str[a] = str[a] + 32;
		}
	a++;
	}
	str[a] = 0;
	return (str);
}
